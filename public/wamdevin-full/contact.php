function showNotification(message, type = 'info') {
	try {
		// Remove existing notifications
		const existingNotifications = document.querySelectorAll('.wamdevin-notification');
		existingNotifications.forEach(notification => {
			notification.remove();
		});
		
		// Create notification element
		const notification = document.createElement('div');
		const alertClass = type === 'success' ? 'alert-success' : type === 'error' ? 'alert-danger' : 'alert-info';
		const icon = type === 'success' ? 'check-circle' : type === 'error' ? 'exclamation-circle' : 'info-circle';
		
		notification.className = `alert ${alertClass} alert-dismissible fade show wamdevin-notification`;
		notification.setAttribute('role', 'alert');
		notification.style.cssText = `
			position: fixed;
			top: 20px;
			right: 20px;
			z-index: 9999;
			max-width: 400px;
			box-shadow: 0 10px 25px rgba(0,0,0,0.15);
			border: none;
			border-radius: 10px;
			animation: slideInRight 0.3s ease;
		`;
		
		notification.innerHTML = `
			<i class="fas fa-${icon}" style="margin-right: 10px;"></i>
			<span>${message}</span>
			<button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="Close"></button>
		`;
		
		document.body.appendChild(notification);
		
		// Auto remove after 5 seconds
		setTimeout(() => {
			if (notification.parentNode) {
				notification.style.animation = 'slideOutRight 0.3s ease';
				setTimeout(() => {
					if (notification.parentNode) {
						notification.remove();
					}
				}, 300);
			}
		}, 5000);
	} catch (error) {
		console.error('Error showing notification:', error);
		// Fallback alert
		alert(message);
	}
}

