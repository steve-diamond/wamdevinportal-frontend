<script>
	// Initialize AOS animations
	AOS.init({
		duration: 1000,
		once: true,
		offset: 100
	});
	
	// Leadership card interactions
	jQuery(document).ready(function($) {
		// Add smooth hover effects for leadership cards
		$('.leadership-card').hover(
			function() {
				$(this).find('.leader-overlay').css('opacity', '1');
			},
			function() {
				$(this).find('.leader-overlay').css('opacity', '0');
			}
		);
		
		// Statistics counter animation
		function animateNumbers() {
			$('.stats-card h3').each(function() {
				var $this = $(this);
				var countTo = $this.text().replace('+', '');
				if (!isNaN(countTo)) {
					$({ countNum: 0 }).animate({
						countNum: countTo
					}, {
						duration: 2000,
						easing: 'linear',
						step: function() {
							$this.text(Math.floor(this.countNum) + '+');
						},
						complete: function() {
							$this.text(this.countNum + '+');
						}
					});
				}
			});
		}
		
		// Trigger animation when stats section is in view
		$(window).scroll(function() {
			$('.stats-card').each(function() {
				var elementTop = $(this).offset().top;
				var viewportTop = $(window).scrollTop();
				var viewportBottom = viewportTop + $(window).height();
				
				if (elementTop < viewportBottom && elementTop > viewportTop) {
					if (!$(this).hasClass('animated')) {
						$(this).addClass('animated');
						animateNumbers();
					}
				}
			});
		});
	});
</script>
