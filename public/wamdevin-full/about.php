        // Navigation menu functionality
        document.addEventListener('DOMContentLoaded', function() {
            // Mobile menu toggle
            const menuToggle = document.querySelector('.navbar-toggler');
            const menuLinks = document.querySelector('.menu-links');
            
            if (menuToggle && menuLinks) {
                menuToggle.addEventListener('click', function() {
                    menuLinks.classList.toggle('show');
                    document.body.classList.toggle('menu-open');
                });
            }

            // Close mobile menu when clicking outside
            document.addEventListener('click', function(e) {
                if (!e.target.closest('.header')) {
                    if (menuLinks) menuLinks.classList.remove('show');
                    document.body.classList.remove('menu-open');
                }
            });

            // Search toggle
            const searchBtn = document.querySelector('.search-btn');
            const searchBar = document.querySelector('.nav-search-bar');
            const searchClose = document.querySelector('#search-remove');
            
            if (searchBtn && searchBar) {
                searchBtn.addEventListener('click', function(e) {
                    e.preventDefault();
                    searchBar.classList.add('active');
                });
            }

            if (searchClose && searchBar) {
                searchClose.addEventListener('click', function(e) {
                    e.preventDefault();
                    searchBar.classList.remove('active');
                });
            }

            // Sticky header on scroll
            window.addEventListener('scroll', function() {
                const header = document.querySelector('.header');
                if (header) {
                    if (window.scrollY > 100) {
                        header.classList.add('scrolled');
                    } else {
                        header.classList.remove('scrolled');
                    }
                }
            });

            // Language dropdown
            const langDropdown = document.querySelector('.custom-dropdown');
            if (langDropdown) {
                langDropdown.addEventListener('click', function(e) {
                    e.preventDefault();
                    const dropdownMenu = this.querySelector('.dropdown-content');
                    if (dropdownMenu) {
                        dropdownMenu.style.display = dropdownMenu.style.display === 'block' ? 'none' : 'block';
                    }
                });
            }

            // Close dropdown when clicking outside
            document.addEventListener('click', function(e) {
                if (!e.target.closest('.custom-dropdown')) {
                    const dropdownMenus = document.querySelectorAll('.custom-dropdown .dropdown-content');
                    dropdownMenus.forEach(menu => menu.style.display = 'none');
                }
            });

            // Mobile dropdown menus
            const navItems = document.querySelectorAll('.navbar-nav li');
            navItems.forEach(item => {
                item.addEventListener('click', function(e) {
                    if (window.innerWidth <= 991) {
                        const subMenu = this.querySelector('.sub-menu');
                        if (subMenu) {
                            e.preventDefault();
                            subMenu.style.maxHeight = subMenu.style.maxHeight === '300px' ? '0' : '300px';
                        }
                    }
                });
            });
        });
